#Entrada
Mujeres=int(input("Numero de Mujeres:"))
Hombres=int(input("Numero de Hombres:"))
#Caja Negra
Alumnos_Total=(Mujeres+Hombres)
Porcentaje_M=(Mujeres/Alumnos_Total)*100#input
Porcentaje_H=(Hombres/Alumnos_Total)*100#input
#Salida
print("Porcentaje de Mujeres;  ",Porcentaje_M)#str
print("Porcentaje de Hombres;  ",Porcentaje_H)#str